part of 'options_cubit.dart';

abstract class OptionsState extends Equatable {
  const OptionsState();

  @override
  List<Object> get props => [];
}

class OptionsLoaded extends OptionsState {
  const OptionsLoaded();

  @override
  List<Object> get props => [];
}

class OptionsLoadInProgress extends OptionsState {
  const OptionsLoadInProgress();

  @override
  List<Object> get props => [];
}
